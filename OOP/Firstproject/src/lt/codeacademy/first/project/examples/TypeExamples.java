package lt.codeacademy.first.project.examples;

public class TypeExamples {
        //lapkricio 14d.
    public static void main(String[] args) {

            // Tipas kintamojo vardas (rasomas is mazosios) = kintamojo reiksme
            //byte
            byte myByteExample = 120;
            System.out.println("byte:" + myByteExample);

            //short
            short myShortExample = 30000;
            System.out.println("short: " + myShortExample);

            //int
            int myIntExample = 1000000;
            System.out.println("int: " + myIntExample);

            //long
            long myLongExample = 50000000000L;
            System.out.println("long: " + myLongExample);

            //float
            float myFloatExample = 2.3f;
            System.out.println("float: " + myFloatExample);

            //double
            double myDoubleExample = 3.57545487547;
            System.out.println("double: " + myDoubleExample);

            //char
            char myCharExample = '\u0046';
            System.out.println("char: " + myCharExample);

            //boolean
            boolean isCarRed = true;
            System.out.println("boolean: " + isCarRed);

            String myStringExample = "Sveiki esu Vilda ir esu pradedancioji programuotoja";
            System.out.println(myStringExample);

        }
}
